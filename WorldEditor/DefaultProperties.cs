namespace MMO3D.WorldEditor
{
    /// <summary>
    /// Default properties for the program options.
    /// </summary>
    public static class DefaultProperties
    {
        /// <summary>
        /// Gets the speed at which the camera moves.
        /// </summary>
        /// <value>See summary.</value>
        public static float CameraMovementSpeed
        {
            get { return 3.18f; }
        }

        /// <summary>
        /// Gets the speed at which the camera is rotated.
        /// </summary>
        /// <value>See summary.</value>
        public static float CameraRotationSpeed
        {
            get { return 3; }
        }
    }
}
