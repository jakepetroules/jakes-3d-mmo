namespace Petroules.Synteza.Math
{
    /// <summary>
    /// Represents types of information units.
    /// </summary>
    public enum InformationUnit
    {
        /// <summary>
        /// Represents bits.
        /// </summary>
        Bit = -3,

        /// <summary>
        /// Represents semi-nibbles.
        /// </summary>
        SemiNibble = -2,

        /// <summary>
        /// Represents nibbles.
        /// </summary>
        Nibble = -1,

        /// <summary>
        /// Represents bytes.
        /// </summary>
        Byte = 0,

        /// <summary>
        /// Represents kilobytes.
        /// </summary>
        Kilobyte = 1,

        /// <summary>
        /// Represents megabytes.
        /// </summary>
        Megabyte = 2,

        /// <summary>
        /// Represents gigabytes.
        /// </summary>
        Gigabyte = 3,

        /// <summary>
        /// Represents terabytes.
        /// </summary>
        Terabyte = 4,

        /// <summary>
        /// Represents petabytes.
        /// </summary>
        Petabyte = 5,

        /// <summary>
        /// Represents exabytes.
        /// </summary>
        Exabyte = 6,

        /// <summary>
        /// Represents zettabytes.
        /// </summary>
        Zettabyte = 7,

        /// <summary>
        /// Represents yottabytes.
        /// </summary>
        Yottabyte = 8
    }
}
