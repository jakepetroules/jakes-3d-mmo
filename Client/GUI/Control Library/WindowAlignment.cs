namespace MMO3D.Client
{
    /// <summary>
    /// Defines window alignments to a particular edge of the screen.
    /// </summary>
    public enum WindowAlignment
    {
        /// <summary>
        /// Aligns the window to the top-left corner of the screen.
        /// </summary>
        TopLeft,

        /// <summary>
        /// Aligns the window to the top-center of the screen.
        /// </summary>
        TopCenter,

        /// <summary>
        /// Aligns the window to the top-right corner of the screen.
        /// </summary>
        TopRight,

        /// <summary>
        /// Aligns the window to the middle-left of the screen.
        /// </summary>
        MiddleLeft,

        /// <summary>
        /// Aligns the window to the center of the screen.
        /// </summary>
        MiddleCenter,

        /// <summary>
        /// Aligns the window to the middle-right of the screen.
        /// </summary>
        MiddleRight,

        /// <summary>
        /// Aligns the window to the bottom-left corner of the screen.
        /// </summary>
        BottomLeft,

        /// <summary>
        /// Aligns the window to the bottom-center of the screen.
        /// </summary>
        BottomCenter,

        /// <summary>
        /// Aligns the window to the bottom-right corner of the screen.
        /// </summary>
        BottomRight
    }
}
